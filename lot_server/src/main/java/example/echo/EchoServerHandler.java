package example.echo;

import io.netty.buffer.ByteBuf;
import io.netty.buffer.Unpooled;
import io.netty.channel.Channel;
import io.netty.channel.ChannelHandler;
import io.netty.channel.ChannelHandler.Sharable;
import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.ChannelInboundHandlerAdapter;
import io.netty.channel.ChannelPipeline;
import io.netty.util.concurrent.EventExecutor;

/**
 * Handler implementation for the echo server.
 */
@Sharable
public class EchoServerHandler extends ChannelInboundHandlerAdapter {

    @Override
    public void channelRead(ChannelHandlerContext ctx, Object msg) {
    }

    @Override
    public void channelReadComplete(ChannelHandlerContext ctx) {
        Channel a = ctx.channel();

        ChannelPipeline b1 = a.pipeline();

        ChannelHandlerContext con = b1.firstContext();

        b1.context("");

        ChannelPipeline b = ctx.pipeline();

        EventExecutor c = ctx.executor();

        ChannelHandler d = ctx.handler();

        System.out.println(System.identityHashCode(this));
        System.out.println(System.identityHashCode(ctx));
        System.out.println(System.identityHashCode(a));
        System.out.println(System.identityHashCode(b));
        System.out.println(System.identityHashCode(c));
        System.out.println(System.identityHashCode(d));


        ByteBuf firstMessage = Unpooled.buffer(ctx.pipeline().toString().getBytes().length);
        firstMessage.writeBytes(ctx.toString().getBytes());
        ctx.writeAndFlush(firstMessage);
    }

    @Override
    public void exceptionCaught(ChannelHandlerContext ctx, Throwable cause) {
        // Close the connection when an exception is raised.
        cause.printStackTrace();
        ctx.close();
    }

}
